async def noti(context: ContextTypes.DEFAULT_TYPE):
    msg = random.choice(notis[context.job.data["t"]])
    db = DB()
    users = db.get_users()
    for user in users:
        if not db.streak(user):
            await context.bot.send_message(chat_id=user, text=msg)
